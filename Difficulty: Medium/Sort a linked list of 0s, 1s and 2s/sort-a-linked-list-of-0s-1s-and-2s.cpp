class Solution {
  public:
    Node* segregate(Node* head) {
        int z=0;
        int o=0;
        int t=0;
        Node *node=head;
        while(node!=NULL){
            if(node->data==0){
                z+=1;
            }
            else if(node->data==1){
                o+=1;
            }
            else{
                t+=1;
            }
            node=node->next;
        }
        node=head;
        while(node!=NULL){
            if(z>0){
                node->data=0;
                z-=1;
            }
            else if(o>0){
                node->data=1;
                o-=1;
            }
            else{
                node->data=2;
                
            }
            node=node->next;
        }
        return head;
    }
};



