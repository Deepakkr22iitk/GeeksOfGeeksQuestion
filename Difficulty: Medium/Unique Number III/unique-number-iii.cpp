class Solution {
  public:
    int getSingle(vector<int> &arr) {
        int N=arr.size();
        unordered_map<int,int> freq;
        for(int i=0;i<N;i++)
        {
            freq[arr[i]]++;
        }
        for(auto &it:freq)
        {
            if (it.second == 1) return it.first;
        }
    }
};


